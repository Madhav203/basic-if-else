// Divisible by 3 or 7
//
class DivisibleBY37{
	public static void main(String[] args){

		int num = 15;

		if(num%3==0){
			System.out.println("Number is divisible by 3");
		} else if (num%7==0){
			System.out.println("Number is divisible by 7");
		} else{
			System.out.println("Number is neither divisible by 3 nor 7");
		}

	}
}


